package ch3;

public class Op3 {

	public static void main(String[] args) {
		int a = 10, b=20;
		
		System.out.println(a == b);			//false
		System.out.println(a != b);	//true		
		System.out.println(a > b);	//false
		System.out.println(a >= b);			//false
		System.out.println(a < b);	//true
		System.out.println(a <= b);			//true
		
	}
}
